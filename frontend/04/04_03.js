// else-if文の例1
let a = 0;
if (a > 0) {
    console.log('a=' + a + 'は、正の整数です'); 
} else if (a < 0) {
    console.log('a=' + a + 'は、負の整数です'); 
} else if (a === 0) {
    console.log('a=' + a + 'は、0です'); 
} else {
    console.log('a=' + a + 'は、判定できません');    
}



