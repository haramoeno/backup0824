// if文の例
let a = 3;

if (a >= 0) {  // if文 変数aが0以上の場合
    console.log('a=' + a + 'は、正の整数です');
}



// if文の例2
let b = -5;

if (b >= 0) {
    console.log('b=' + b + 'は、正の整数です');
    // 条件式を満たしていないため表示されない
}




// if-else文の例
let c = -5;
if (c >= 0) {
    console.log('c=' + c + 'は、正の整数です'); 
} else {
    console.log('c=' + c + 'は、負の整数です'); 
}